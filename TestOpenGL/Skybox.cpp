void Skybox::LoadCubemapTexture()
{
	std::vector<std::string> faces
	{
		"right.jpg","left.jpg","top.jpg",
		"bottom.jpg","front.jpg","back.jpg"
	};

	cubemapTexture = Texture::LoadCubemap(faces);
}

Skybox::~Skybox()
{
	delete vertices;
	delete indices;
}

size_t Skybox::VerticeSize() const
{
	return sizeof(float) * 108;
}

size_t Skybox::IndiceSize() const
{
	return sizeof(indices) * 6;
}

void Skybox::SetShader(Shader& shader)
{
	shapeShader = new Shader(shader);
}

void Skybox::InitBuffer()
{
	glGenVertexArrays(1, &vao);
	glGenBuffers(1, &vbo);
	glBindVertexArray(vao);
	glBindBuffer(GL_ARRAY_BUFFER, vbo);
	glBufferData(GL_ARRAY_BUFFER, sizeof(float) * 108, vertices, GL_STATIC_DRAW);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
}

void Skybox::Draw()
{
	glBindVertexArray(vao);
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_CUBE_MAP,cubemapTexture);
	glDepthFunc(GL_LEQUAL);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glDepthFunc(GL_LESS);
}