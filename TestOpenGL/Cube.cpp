Cube::~Cube()
{
	delete vertices;
	delete indices;
}

size_t Cube::VerticeSize() const
{
	return sizeof(float) * 288;
}

float* Cube::Vertices() const
{
	return vertices;
}

size_t Cube::IndiceSize() const
{
	return sizeof(indices) * 6;
}

int* Cube::Indices() const
{
	return indices;
}
